package com.example;

public class Figures {
    public int NumCorners;
    public String name;

    public Figures() {
    }

    public int getNumCorners() {
        return NumCorners;
    }

    public void setNumCorners(int NumCorners) {
        this.NumCorners = NumCorners;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

}
